package Heranca;

public class Animal {

	private String nome;
	private boolean racional;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public boolean isRacional() {
		return racional;
	}
	public void setRacional(boolean racional) {
		this.racional = racional;
	}
}
